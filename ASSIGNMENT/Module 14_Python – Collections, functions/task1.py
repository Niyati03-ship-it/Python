# Write a Python program to access elements at different index positions

list = [10, "Hello", 3.14, True, "Python"]
print("Element at index 0:", list[0])   # First element
print("Element at index 2:", list[2])   # Third element
print("Element at index -1:", list[-1]) # Last element
print("Element at index -3:", list[-3]) # Third from last element
print("Elements from index 1 to 3:", list[1:4])  # From index 1 to 3
print("Elements from start to index 2:", list[:3]) # From start to index 2
print("Elements from index 2 to end:", list[2:])  # From index 2 to end


